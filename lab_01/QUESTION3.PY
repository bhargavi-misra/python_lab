int_num = 10
float_num = 5.5
complex_num = 3 + 4j

int_add = int_num + 5
float_sub = float_num - 2.5
complex_mul = complex_num * (2 + 3j)

str1 = "Hello"
str2 = "World"
str_concat = str1 + " " + str2

list1 = [1, 2, 3]
list2 = [4, 5, 6]
list_concat = list1 + list2

print(f"Integer addition: {int_add}")
print(f"Float subtraction: {float_sub:.2f}")
print(f"Complex multiplication: {complex_mul}")
print(f"String concatenation: {str_concat}")
print(f"List concatenation: {list_concat}")
